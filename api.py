def location_worker(user_pos, *, flag_iso=True, cfg_cluster_dist, cfg_tpersistence):
    # store timestamp
    tic = datetime.datetime.now()

    # generate "unique" prefix for image files
    tstr = tic.strftime('%Y%m%dT%H%M%S.%f')
    fprefix = f'img_{tstr}_'

    # no datafile given --> use DB for current positions
    r = main(
            observer_pos=user_pos, obj_path=Path('/home/cl/work/criticalmaps--richtungspfeil/objs/'), fprefix=fprefix,
            exclude_isolated_points=flag_iso, cluster_dist_thres=cfg_cluster_dist, cluster_trace_persistence=cfg_tpersistence
    )

    diag_info = '<h3>Diag Infos</h3>'
    diag_info += f'Server time: {tstr}<br>'
    diag_info += '<ul>'
    for di in r['diag_info']:
        diag_info += '<li>'+di
    diag_info += '</ul>'


    def ci_as_table(lci):
        """
        Converts list of ClusterInfo instances to HTML table
        """
        r = "<table>\n"
        if len(lci)>0:
            r += lci[0].table_header()
            for x in lci:
                r += x.as_html() + '\n'
        else:
            r += '<tr><td><font color=red>(no clusters match current criteria)</font></td></tr>'
        r += "</table>\n"
        return r

    ci = r['clusters']
    ci = sorted(ci, key=lambda _: (_.N,-_.dist), reverse=True) # Note: tuple as sort key -> force sort order for clusters with identical N, list closer clusters first (if first element is identical, second element determines sort order)
    ci_dist = sorted(ci, key=lambda _: _.dist, reverse=False)
    html_info  = ''
    html_info += '<h3>Clusters (lowest distance first)</h3>'
    html_info += ci_as_table(ci_dist)
    html_info += '<h3>Clusters (largest cluster first)</h3>'
    html_info += ci_as_table(ci)

    # Keys of images to present FIRST (in this order)
    top_files = ['clusters_polar','clusters_local','clusters']

    html_imgs = ""
    print(r['files'])
    def html4img(k):
        def get_img_loc(k):
            return 'api/objs/' + r['files'][k]
        return f'<img src="{ get_img_loc(k) }"><br>'
    ### top imgs
    for k in top_files:
        if not k in r['files']:
            print(f'WARNING: key {k} not found in list of files')
            continue
        html_imgs += html4img(k)
    ### all other images
    for k in r['files'].keys():
        if k in top_files:
            continue
        html_imgs += html4img(k)

    # store timestamp
    toc = datetime.datetime.now()
    deltat = toc-tic
    deltat = deltat.total_seconds()
    diag_info += f'<p>total processing time: {deltat:.2f} seconds'

    return {
        'html': html_info+'<h3>Plots generated by server</h3>'+html_imgs,
        'diag': diag_info
    }

